package store.aurora.config.security.authProvider.daoAuthProvider;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;
import store.aurora.user.domain.Role;

import java.util.Arrays;
import java.util.Collection;

public class ApiUserDetails implements UserDetails {

    private String username;
    private String password;
    private Role role;

    public ApiUserDetails(String username, String password, Role role) {
        this.username = username;
        this.password = password;
        this.role = role;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return Arrays.asList(new SimpleGrantedAuthority(role.name()));
    }

    @Override
    public String getPassword() {
        return this.password;
    }

    @Override
    public String getUsername() {
        return this.username;
    }
}
